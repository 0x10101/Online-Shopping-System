create table users(
	emailid		text primary key,
	passwd		text not null,
	name		text,
	address		text,
	phone		text
);

create table products(
	product_id	serial primary key,
	product_desc	text,
	product_rate	float,
	qty_in_hand	int,
	disc		float
);

create table order_master(
	order_id	serial primary key,
	order_date	date,
	total		float,
	status		text,
	pay_mode	text,
	card_no		text,
	bank_name	text,
	emailid		text references users(emailid)
);

create table order_details(
	product_id	int references products(product_id),
	order_id	int references order_master(order_id),
	qty		int,
	size		text,
	color		text,
	primary key(product_id,order_id)
);

